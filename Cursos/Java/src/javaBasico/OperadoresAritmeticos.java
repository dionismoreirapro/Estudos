package javaBasico;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		int a = 5;
		double x = 8.0;
		
		
		
		System.out.println(a + x);
		System.out.println(a * x);
		System.out.println(a / x);
		System.out.println(a - x);
		System.out.println(a % x);
		
		System.out.println("------------------------------------");
		int r = 3 - 4 * 12;
		double h = Math.pow(r, 2);
		
		System.out.println(r);
		System.out.println(h);

	}

}
